package jd_03;

public class Rectangle {

    public static void main(String[] args) {

        double length = 4.5;
        double width = 2;
        double area;
        double perimeter;

        area = length * width;

        perimeter = 2 * (length + width);

        System.out.println("The area of the rectangle is " + area);
        System.out.println("The perimeter of the rectangle is " + perimeter);



    }
}
